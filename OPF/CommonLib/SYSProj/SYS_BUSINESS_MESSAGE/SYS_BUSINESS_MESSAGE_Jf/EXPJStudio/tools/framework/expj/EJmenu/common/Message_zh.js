/**
 * Error message display<br>
 * <br>
 * Error messages are displayed with alert. <br>
 *
 * @param errorCodes Error code
 */
function displayErrorMessage(errorCodes) {
    var errorMessage = "";
    var eStr = "";
    if (typeof(errorCodes) == "string") {
        errorMessage = "[" + errorCodes + "]" + getMessageOnly(errorCodes);
        eStr = eStr + "msg%%[" + errorCodes + "] " + getMessageOnly(errorCodes) + "&color%%FF0000&icontype%%ERROR";
    } else {
        for (var i = 0; i < errorCodes.length; i++) {
            if (errorMessage.length > 0) {
                errorMessage += "\n";
                eStr += "|";
            }
            errorMessage +=  "[" + errorCodes[i] + "]" + getMessageOnly(errorCodes[i]);
            eStr += "msg%%[" + errorCodes[i] + "] " + getMessageOnly(errorCodes[i]) + "&color%%FF0000&icontype%%ERROR";
        }
    }
    parent.setMessage(eStr);
    alert(errorMessage);
}

