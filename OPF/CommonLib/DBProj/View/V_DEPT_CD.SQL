CREATE OR REPLACE VIEW V_DEPT_CD
    (COMPANY_CD,DEPT_CD,DEPT_NAME)
AS
SELECT  COMPANY_CD
       ,ORG_CD
       ,ORG_NAME
FROM    M_ORG
/
COMMENT ON TABLE V_DEPT_CD IS '負担部門View'
/
COMMENT ON COLUMN V_DEPT_CD.COMPANY_CD IS '会社コード'
/
COMMENT ON COLUMN V_DEPT_CD.DEPT_CD IS '負担部門コード'
/
COMMENT ON COLUMN V_DEPT_CD.DEPT_NAME IS '負担部門名'
/
